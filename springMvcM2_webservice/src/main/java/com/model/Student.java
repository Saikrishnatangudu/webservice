package com.model;

public class Student {
	
	private int stuId ;
	private String stuName;
	public Student() {
		super();
		
	}
	public int getStuId() {
		return stuId;
	}
	public void setStuId(int stuId) {
		this.stuId = stuId;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	

}
